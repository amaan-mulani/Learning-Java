class DemoSelectionConstructs
{
	public static void main(String[] args)
	{
		int age=5;	
		
		if(age == 1)
		{		
			System.out.println("One");
		}
		else if(age == 2)
		{		
			System.out.println("Two");
		}
		else if(age == 3)
		{		
			System.out.println("Three");
		}
		else 
		{
			System.out.println("Incorrect Value");
		}
	}
}