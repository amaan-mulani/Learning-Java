class DemoVariable
{
	public static void main(String[] args)
	{	
		int rollNumber=10;
		float marks=80.80f;
		char c= 'A';
		boolean result= true;

		System.out.println("Variable Demo");
		System.out.println(rollNumber);
		System.out.println(marks);
		System.out.println(c);
		System.out.println(result);
	}
}